int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	ret;
	int	i;

	i = 1;
	while (*s1 != '\0' && i != n)
	{
		if (*s1 != *s2)
		{
			ret = *s1 - *s2;
			return (ret);
		}
		s1++;
		s2++;
		i++;
	}
	ret = *s1 - *s2;
	return (ret);
}
